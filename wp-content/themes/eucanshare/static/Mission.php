	<h2 class="section-title">Our mission</h2>
		<p>
		euCanSHare is <strong>a joint EU-Canada project to establish a cross-border data sharing and multi-cohort cardiovascular research platform</strong>.
		Specifically, the project will integrate data infrastructures, IT solutions and data sources from EU, Canada and other countries into a web-based data access system with functionalities for increased efficiency in cardiovascular data-driven research. euCanSHare integrates more than 35 Canadian and European cohorts making up over 1 million records and actively seeks to expand to other regions. <br><br>euCanSHare key objectives are
		</p>
		
