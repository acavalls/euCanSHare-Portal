			Despite continuous advances in diagnosis and treatment, <strong>cardiovascular diseases (CVDs) remain the main cause of death worldwide</strong>. CVDs account for about 17.9 million annual deaths<sup>1</sup> – 31% of all deaths worldwide – and greatly reduce the quality of life of affected patients, challenging the sustainability of modern healthcare systems. In Europe, CVDs are responsible for 30.4% and 25.3% of deaths before the age of 65, in men and women, respectively.<sup>2</sup><br><br>
			<strong>Many cardiovascular drugs have shown limited efficacy on general populations</strong>. Personalised medicine approaches offer solutions to <strong>improve risk assessment, early diagnosis and patient-tailored treatment protocols</strong>. Data-driven, multi-cohort approaches are needed to link molecular, imaging, functional and clinical data. However, such integration presents a formidable challenge in terms of data storage and access frameworks, interoperability and IT architectures, especially across diverse jurisdictions. 
